/**
 * Generate a single-screen WhatsApp-shareable text summary of the report.
 * Layer 1 of the three-layer report architecture (per ADR-024).
 */
export function generateWhatsAppForward(input: WhatsAppForwardInput): string {
  const topInsights = extractTopInsights(input.riskInsights, 3);
  const riskBadge = input.atRiskAmount
    ? `⚠️ At-risk: ${input.atRiskAmount}`
    : "✅ Looks clean";
  const ownerLine = input.bhulekhUsable && input.ownerName && input.ownerName !== "—"
    ? `Owner: ${input.ownerName}`
    : "Owner: verify from Bhulekh";
  const reportUrl = input.reportUrl
    ? `\n\n📋 Full report + details: ${input.reportUrl}`
    : "";

  const insightLines = topInsights.map((insight) => {
    const icon = insight.severity === "redFlag" ? "🔴" : insight.severity === "watchout" ? "🟡" : "🟢";
    return `${icon} ${insight.label}`;
  }).join("\n");

  const footer = "\n\n— Generated by ClearDeed | Verify before any transaction";

  return [
    `📍 ${input.plotVillage}, ${input.plotTahasil} (Plot ${input.plotNo})`,
    `${riskBadge} | Area: ${input.plotAreaSummary || "verify from Bhulekh"}`,
    ownerLine,
    "",
    insightLines || "Run a full ClearDeed report for complete details.",
    reportUrl,
    footer,
  ].filter(Boolean).join("\n");
}

function extractTopInsights(riskInsights: Record<string, any[]>, max = 3): Array<{
  label: string;
  severity: string;
}> {
  const SEVERITY_ORDER: Record<string, number> = {
    redFlag: 0,
    watchout: 1,
    positive: 2,
  };
  const all: Array<{ label: string; severity: string; priority: number }> = [];
  for (const group of Object.values(riskInsights)) {
    for (const item of group as any[]) {
      all.push({ label: item.label ?? "", severity: item.severity ?? "watchout", priority: item.priority ?? 50 });
    }
  }
  return all
    .sort((a, b) => {
      const sevDiff = (SEVERITY_ORDER[a.severity] ?? 3) - (SEVERITY_ORDER[b.severity] ?? 3);
      if (sevDiff !== 0) return sevDiff;
      return a.priority - b.priority;
    })
    .slice(0, max);
}

